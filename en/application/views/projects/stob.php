                        <h2>Succession and Transfer of Business in Regions</h2>
                        <h2>Project summary</h2>
                            <p>Transfers of business (ToB) is a crucial phase in business as well as in the entrepreneur´s life cycle. The importance of ToB for jobs and growth can be demonstrated with the estimated number of 450.000 businesses, providing more than 2 million jobs which are transferred every year (see Entrepreneurship 2020 Action Plan). Much attention has been given to starting new businesses, but for growth and jobs it is equally important to ensure the continuation of existing companies.</p>
                        <p>We can observe the same trends in the EU:</p>
                            <ul class="about_list">
                                <li>an increased importance of ToB due to the growing number of transfers resulting from demographic change in Europe (but also a declining number of potential buyers as a result of the shrinking population)</li>
                                <li>an increasing number of ToB will take place outside the family to third parties</li>
                                <li>an growing number of entrepreneurs will only stay in the same company for a shorter period of time (not for a life time)</li>
                                <li>Personal decisions (change of interests or in the family situation, early retirement) will trigger transfers </li>
                            </ul>

                        <p>It is often forgotten that starting a new firm is not the only way to become an entrepreneur. Taking over a business is a real alternative for aspiring entrepreneurs to start a business. Safeguarding existing companies has a higher impact on jobs and growth than start-ups: the success rate of transfers is higher than that of start-ups and transfers conserve in average more jobs. </p>
                        <p>A transfer involves complex issues like valuation, matchmaking and financing (see workshop topics). STOB regions addresses the complexity of the whole transfer process from the first thoughts of an owner to transfer the business till the actual transfer. The project partners will also discuss the challenges regional authorities face while implementing regional policies and ESIF instruments to support business transfer. </p>
                        <p>In the learning process the partners will also have a closer look on the differences between the partners from Eastern and Western Europe. Enterprises in Eastern Europe were mostly founded after the political changes in the early nineties. For the first time these businesses are now and in the coming years in the situation to look for a successor/buyer. Traditional SMEs, in particular family businesses, in Western Europe often were transferred at least one time in their existence. This implies that the experiences in the regions of the STOB regions partnership are different which will lead to a fruitful exchange between the partners.</p>
                        <p>The smallest enterprises are the most vulnerable to failed transfer. These micro enterprises are often closely related to their owner´s skills. The low value and the size of the business often hamper transfers. Considering the fact that 9 out of 10 are SMEs with fewer than 10 employees and a Commission´s study from 2011 on "Business Dynamics" estimated the loss of non-efficient transfers on 150.000 companies and 600.000 jobs per year clearly shows the importance for regions to have a closer look on the topic of business transfer.</p>
                        <p>STOB regions contributes to Cohesion Policy and the Europe 2020 Strategy and its goals to  create growth and jobs as well as the implementation of the Entrepreneurship 2020 Action Plan (COM (2012) 795 final) Pillar 2, Create an environment where entrepreneurs can flourish and grow, key area 4 - Easier business transfer.</p>
                        <p>The project contributes to the objectives of the priority axis 2 of INTERREG Europe programme. The CP regards it as crucial to respond adequately to the key challenges that obstruct businesses on their path to growth. Successful ToB is such a challenge.

                        </p>
                        <p>Through having a holistic look on the transfer process the project enhances the capacity of MAs/IBs of ESIF programmes to set up new initiatives helping to improve the success of ToB.</p>
                        <p>STOB regions aims to optimise the implementation of the ESI Funds in the regions in the field of companies succession and transfer of business through initiating a learning process, exchanging experiences, evaluating, transferring and up-scaling the good practices in the partner regions.</p>
                        <p>With the results of the project regional stakeholders on all levels will be enabled to see the whole transfer process and to improve projects and their regional strategic approach to the topic applying a holistic approach.</p>
                        <p>The sub-objectives are:</p>
                        <p>SO 1 -  To address crucial issues in business transfers and generation shift such as: Sensitisation and raising awareness and barriers for a transfer, psychology of company succession; Family Businesses; Advisory services for the whole transfer process; Valuation, rating and maintenance of value of companies in the transfer process; Buyer from universities/ Employee buy-out / Migrant buyers / Female Buyers / Buyers from other countries (internationalisation and ToB); Matchmaking; Business Transfer in rural areas; Financing / Financial instruments to support TOB; Knowledge transfer / Innovation.</p>
                        <p>SO 2 - To contribute in a measurable way to regional growth through the business transfers and regional action plans.</p>
                        <p>SO 3 - To benefit from interregional policy learning on all levels (from regional government to 	stakeholders)</p>
                        <p>SO 4 - To benefit from mobilising the regional stakeholders and include their expertise </p>
                        <p>SO 5 - To mobilise expertise and multi-funding sources for new projects supporting the transfer of 	business</p>
                        <p>SO 6 - To develop and implement peer reviewed action plans and to engage stakeholders in the 	process</p>
                        <p>SO 7 - To raise awareness on the topic and to disseminate the project at regional and EU level</p>
                        <p>SO 8 - To ensure smooth and effective project management</p>

                        <p>Project Approach</p>
                        <p>STOP regions project activities base on interregional policy learning events during 6 semesters as the core element to achieve the objectives of the project. The interregional learning and exchange follows the logic of business transfer as a process which takes at least five years from the first thoughts of an owner to sell the business till the actual transfer.</p>
                        <p>In the first semester every region will prepare an inception report. The baseline study describes the regional situation and provides good practices, bad experiences or failures, regional barriers and drivers etc. as a basis for the action plan.</p>
                        <p>Interregional policy learning workshops will cover all aspects of the transfer process as well as other relevant specific issues: </p>
                        <ul class="about_list">
                            <li>sensitization and raising awareness and barriers for a transfer, psychology of company succession</li>
                            <li>family businesses</li>
                            <li>advisory services for the whole transfer process</li>
                            <li>valuation, rating and maintenance of value of companies in the transfer process</li>
                            <li>buyer from universities / employee buy-out / Migrant buyers / Female Buyers / Buyers from other countries (internationalisation of ToB)</li>
                            <li>matchmaking</li>
                            <li>Business Transfer in rural areas</li>
                            <li>financing/financial instruments to support TOB</li>
                            <li>knowledge transfer/innovation</li>
                        </ul>
                        <p>The main output will be the elaboration and implementation of the 9 action plans for all participating regions.</p>
                        <p>The following results can be expected from the interregional policy learning process and the intense cooperation:</p>
                        <ul class="about_list">
                            <li>Increased political commitment and awareness to support transfer of business as an crucial factor for achieving growth and jobs</li>
                            <li>Increased capacity of the MAs/IBs of ESIFs and regional authorities on policy interventions, new initiatives, methodology and tools in the policy area of business transfer</li>
                            <li>More effective governance and implementation of the ESIF programmes</li>
                            <li>A holistic view on the process and the requirements and obstacles of transfer of business </li>
                            <li>A stable network in the regions between the partners, the policy makers and the regional stakeholders
                            </li>
                        </ul>
