                            <li>намирането на биснес партньори</li>
                            <li>трасфер на бизнес в селски райони</li>
                            <li>финансиране / финасови инструменти в подкрепа на трансфера на бизнес</li>
                            <li>трансфер на знание / иновации</li>
                        </ul>

                        <p>Основният резултат ще бъде изработването и изпълнението на 9 планове за действие за всички участващи региони.</p>
