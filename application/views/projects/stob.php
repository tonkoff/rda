                        <p>Проектът STOB regions активира  дейности относно междурегионалната политиката по време на 6-те семестъра като основен елемент за постигане на целите на проекта. </p>
                        <p>Междурегионалните работни семинарни дейности ще обхванат всички аспекти на процеса на прехвърляне, както и други свързани с конкретни въпроси:</p>
